//subtract , multiply, divide and squar
const add = (a, b) => {
  return a + b;
};
const subtract = (a, b) => {
  return a - b;
};
const multiply = (a, b) => {
  return a * b;
};
const divide = (a, b) => {
  return a / b;
};

let a = add(15, 5);
let b = subtract(15, 5);
let c = multiply(15, 5);
let d = divide(15, 5);
console.log("add " + a, "subtract " + b, "multiply " + c, "divide " + d);
